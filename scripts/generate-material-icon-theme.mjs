import { existsSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
// import { dirname, join, resolve } from 'node:path';
import { join, resolve } from 'node:path';
import { createRequire } from 'node:module';
import ts from 'typescript';

const require = createRequire(import.meta.url);
const materialRepoPath = resolve(
  process.env.MATERIAL_ICON_THEME_PATH ??
    '/mnt/d/Workspace/vscode-material-icon-theme',
);
const outputPath = resolve(
  process.env.MATERIAL_ICON_THEME_OUTPUT ??
    'src/material-icon-theme/generated.ts',
);

if (!existsSync(materialRepoPath)) {
  throw new Error(`Material Icon Theme repo not found: ${materialRepoPath}`);
}

require.extensions['.ts'] = (module, filename) => {
  const source = readFileSync(filename, 'utf8');
  const result = ts.transpileModule(source, {
    compilerOptions: {
      esModuleInterop: true,
      module: ts.ModuleKind.CommonJS,
      target: ts.ScriptTarget.ES2020,
    },
    fileName: filename,
  });
  module._compile(result.outputText, filename);
};

const { generateManifest } = require(
  join(materialRepoPath, 'src/core/generator/generateManifest.ts'),
);
const { getDefaultConfig } = require(
  join(materialRepoPath, 'src/core/generator/config/defaultConfig.ts'),
);
const sharedSvg = require(
  join(materialRepoPath, 'src/core/generator/shared/svg.ts'),
);
const { generateFileIcons } = require(
  join(materialRepoPath, 'src/core/generator/fileGenerator.ts'),
);
const { generateFolderIcons, generateRootFolderIcons } = require(
  join(materialRepoPath, 'src/core/generator/folderGenerator.ts'),
);

const manifest = generateManifest();
const defaultIconSvgByName = {};
sharedSvg.writeSVGFiles = async (iconName, svg) => {
  defaultIconSvgByName[iconName] = svg;
};
const config = getDefaultConfig();
await generateFileIcons(config.files.color, config.opacity, config.saturation);
await generateFolderIcons(
  config.folders.color,
  config.opacity,
  config.saturation,
);
await generateRootFolderIcons(
  config.rootFolders.color,
  config.opacity,
  config.saturation,
);

const iconsPath = join(materialRepoPath, 'icons');
const iconSvgByName = Object.fromEntries(
  [
    ...Object.entries(defaultIconSvgByName),
    ...readdirSync(iconsPath)
      .filter((filename) => filename.endsWith('.svg'))
      .sort((a, b) => a.localeCompare(b))
      .map((filename) => [
        filename.slice(0, -'.svg'.length),
        readFileSync(join(iconsPath, filename), 'utf8'),
      ]),
  ].sort(([a], [b]) => a.localeCompare(b)),
);

const generatedAt = new Date().toISOString();
const sourcePackage = JSON.parse(
  readFileSync(join(materialRepoPath, 'package.json'), 'utf8'),
);

const pickRecord = (value) => value ?? {};
const generated = `/* eslint-disable */
// Generated by scripts/generate-material-icon-theme.mjs.
// Source: ${materialRepoPath}
// Source package: ${sourcePackage.name}@${sourcePackage.version}
// Generated at: ${generatedAt}

export interface MaterialIconThemeManifest {
  file?: string;
  folder?: string;
  folderExpanded?: string;
  rootFolder?: string;
  rootFolderExpanded?: string;
  fileExtensions: Record<string, string>;
  fileNames: Record<string, string>;
  folderNames: Record<string, string>;
  folderNamesExpanded: Record<string, string>;
  rootFolderNames: Record<string, string>;
  rootFolderNamesExpanded: Record<string, string>;
  languageIds: Record<string, string>;
}

export const materialIconThemeManifest: MaterialIconThemeManifest = ${JSON.stringify(
  {
    file: manifest.file,
    folder: manifest.folder,
    folderExpanded: manifest.folderExpanded,
    rootFolder: manifest.rootFolder,
    rootFolderExpanded: manifest.rootFolderExpanded,
    fileExtensions: pickRecord(manifest.fileExtensions),
    fileNames: pickRecord(manifest.fileNames),
    folderNames: pickRecord(manifest.folderNames),
    folderNamesExpanded: pickRecord(manifest.folderNamesExpanded),
    rootFolderNames: pickRecord(manifest.rootFolderNames),
    rootFolderNamesExpanded: pickRecord(manifest.rootFolderNamesExpanded),
    languageIds: pickRecord(manifest.languageIds),
  },
  null,
  2,
)};

export const materialIconThemeSvgByName: Record<string, string> = ${JSON.stringify(
  iconSvgByName,
  null,
  2,
)};
`;

writeFileSync(outputPath, generated);
console.log(
  `Generated ${outputPath} from ${sourcePackage.name}@${sourcePackage.version}`,
);
console.log(`Included ${Object.keys(iconSvgByName).length} SVG icons`);
